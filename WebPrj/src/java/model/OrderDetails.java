package model;

/**
 *
 * @author thanh
 */
public class OrderDetails {
    private int id, pid, quantity;
    private double total;

    public OrderDetails() {
    }

    public OrderDetails(int id, int pid, int quantity, double total) {
        this.id = id;
        this.pid = pid;
        this.quantity = quantity;
        this.total = total;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
    
    
}
